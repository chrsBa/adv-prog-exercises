// Task 8: Streams from Vectors.
// Define a trait StreamableOnce that provides a method to consume any type to
// produce a corresponding Stream. Provide an implementation for Vec. Use it to
// define the stream of magic numbers [22, 13, 42].
// -----------------------------------------------------------------------------
pub trait StreamableOnce<A> {
    fn stream_once(self) -> impl Stream<A>;
}

struct VecOnceStream<A> {
    iter: std::vec::IntoIter<A>,
}

impl<A> Stream<A> for VecOnceStream<A> {
    fn next(&mut self) -> Option<A> {
        self.iter.next()
    }
}

impl<A> StreamableOnce<A> for Vec<A> {
    fn stream_once(self) -> impl Stream<A> {
        VecOnceStream {
            iter: self.into_iter(),
        }
    }
}

pub fn magic_numbers() -> impl Stream<usize> {
    vec![22_usize, 13, 42].stream_once()
}

